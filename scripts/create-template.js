/**
 * Generates the measurement-plan-template.xlsx with 9 sheets.
 * Run: node scripts/create-template.js
 */
const ExcelJS = require('exceljs');
const path = require('path');

async function createTemplate() {
  const wb = new ExcelJS.Workbook();
  wb.creator = 'Web Analytics Measurement Plan Agent';
  wb.created = new Date();

  const HEADER_FILL = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF1E3A5F' } };
  const HEADER_FONT = { bold: true, color: { argb: 'FFFFFFFF' }, size: 11 };
  const SECTION_FILL = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF0F2A4A' } };
  const SECTION_FONT = { bold: true, color: { argb: 'FFFFFFFF' }, size: 12 };
  const BORDER = { style: 'thin', color: { argb: 'FFD0D5DD' } };
  const allBorders = { top: BORDER, bottom: BORDER, left: BORDER, right: BORDER };

  function styleHeaders(sheet, row) {
    const r = sheet.getRow(row);
    r.eachCell(c => { c.fill = HEADER_FILL; c.font = HEADER_FONT; c.alignment = { vertical: 'middle', wrapText: true }; c.border = allBorders; });
    r.height = 30;
  }

  function sectionRow(sheet, row, text, cols) {
    const r = sheet.getRow(row);
    r.getCell(1).value = text;
    for (let i = 1; i <= cols; i++) { r.getCell(i).fill = SECTION_FILL; r.getCell(i).font = SECTION_FONT; r.getCell(i).border = allBorders; }
    r.height = 32;
    sheet.mergeCells(row, 1, row, cols);
  }

  // ═══ SHEET 1: Instructions ═══
  const s1 = wb.addWorksheet('1. Instructions', { properties: { tabColor: { argb: 'FF3B82F6' } } });
  s1.columns = [{ width: 25 }, { width: 80 }];
  sectionRow(s1, 1, 'MEASUREMENT PLAN — INSTRUCTIONS', 2);
  s1.getCell('A3').value = 'Purpose';
  s1.getCell('B3').value = 'This workbook is a comprehensive measurement plan generated for your website. It covers objectives, KPIs, metrics, data sources, event tracking, reporting, and governance.';
  s1.getCell('A5').value = 'How to use';
  s1.getCell('B5').value = '1. Review "Project Overview" for context\n2. Validate "Objectives & KPIs" with stakeholders\n3. Use "Tracking Plan" as the implementation spec for developers\n4. Share "Reporting Cadence" with report consumers\n5. Get sign-off via "RACI & Sign-off" sheet';
  s1.getCell('B5').alignment = { wrapText: true };
  s1.getCell('A7').value = 'Sheet overview';
  s1.getCell('B7').value = 'Sheet 1: Instructions (this sheet)\nSheet 2: Project Overview\nSheet 3: Objectives & KPIs\nSheet 4: Metrics & Dimensions\nSheet 5: Data Sources\nSheet 6: Tracking Plan\nSheet 7: Reporting Cadence\nSheet 8: RACI & Sign-off\nSheet 9: Glossary';
  s1.getCell('B7').alignment = { wrapText: true };
  s1.getCell('A9').value = 'Generated by';
  s1.getCell('B9').value = 'Web Analytics Measurement Plan Agent — Powered by Claude AI';
  s1.getCell('A10').value = 'Date';
  s1.getCell('B10').value = new Date().toLocaleDateString('en-US');

  // ═══ SHEET 2: Project Overview ═══
  const s2 = wb.addWorksheet('2. Project Overview', { properties: { tabColor: { argb: 'FF7C3AED' } } });
  s2.columns = [{ width: 25 }, { width: 60 }];
  sectionRow(s2, 1, 'PROJECT OVERVIEW', 2);
  const overviewFields = [
    ['Project / Initiative', ''],
    ['Status', 'Draft'],
    ['Version', '1.0'],
    ['Document Owner', ''],
    ['Stakeholders', ''],
    ['Start Date', ''],
    ['Target Go-Live', ''],
    ['Last Updated', ''],
  ];
  overviewFields.forEach(([k, v], i) => {
    const row = 3 + i;
    s2.getCell(`A${row}`).value = k;
    s2.getCell(`A${row}`).font = { bold: true };
    s2.getCell(`B${row}`).value = v;
    s2.getCell(`A${row}`).border = allBorders;
    s2.getCell(`B${row}`).border = allBorders;
  });
  sectionRow(s2, 12, 'PROJECT BACKGROUND', 2);
  s2.getCell('A14').value = '';
  s2.getCell('A14').alignment = { wrapText: true };

  // ═══ SHEET 3: Objectives & KPIs ═══
  const s3 = wb.addWorksheet('3. Objectives & KPIs', { properties: { tabColor: { argb: 'FF10B981' } } });
  s3.columns = [
    { header: 'Obj ID', key: 'objId', width: 8 },
    { header: 'Business Objective', key: 'objective', width: 30 },
    { header: 'SMART Goal', key: 'goal', width: 35 },
    { header: 'KPI Name', key: 'kpiName', width: 25 },
    { header: 'KPI Definition', key: 'kpiDef', width: 30 },
    { header: 'Formula / Calculation', key: 'formula', width: 30 },
    { header: 'Baseline', key: 'baseline', width: 12 },
    { header: 'Target', key: 'target', width: 12 },
    { header: 'Target Date', key: 'targetDate', width: 14 },
    { header: 'Priority', key: 'priority', width: 10 },
  ];
  sectionRow(s3, 1, 'BUSINESS OBJECTIVES & KEY PERFORMANCE INDICATORS', 10);
  // Re-set headers on row 4 (row 1 is section, rows 2-3 are spacing)
  s3.getRow(3).values = ['', 'Map each business objective to measurable KPIs with clear targets'];
  s3.getRow(4).values = ['Obj ID', 'Business Objective', 'SMART Goal', 'KPI Name', 'KPI Definition', 'Formula / Calculation', 'Baseline', 'Target', 'Target Date', 'Priority'];
  styleHeaders(s3, 4);

  // ═══ SHEET 4: Metrics & Dimensions ═══
  const s4 = wb.addWorksheet('4. Metrics & Dimensions', { properties: { tabColor: { argb: 'FF06B6D4' } } });
  sectionRow(s4, 1, 'METRICS REGISTRY', 8);
  s4.getRow(3).values = ['', 'Supporting metrics that feed into the KPIs above'];
  s4.columns = [
    { width: 8 }, { width: 25 }, { width: 35 }, { width: 10 },
    { width: 20 }, { width: 30 }, { width: 12 }, { width: 18 },
  ];
  s4.getRow(5).values = ['ID', 'Metric Name', 'Definition', 'Unit', 'Linked KPIs', 'Calculation', 'Type', 'Owner'];
  styleHeaders(s4, 5);

  sectionRow(s4, 20, 'CUSTOM DIMENSIONS & USER PROPERTIES', 5);
  s4.getRow(21).values = ['ID', 'Dimension Name', 'Description', 'Scope', 'Example Values'];
  styleHeaders(s4, 21);

  // ═══ SHEET 5: Data Sources ═══
  const s5 = wb.addWorksheet('5. Data Sources', { properties: { tabColor: { argb: 'FFFBBF24' } } });
  sectionRow(s5, 1, 'DATA SOURCES & INTEGRATIONS', 9);
  s5.columns = [
    { width: 8 }, { width: 22 }, { width: 15 }, { width: 30 },
    { width: 15 }, { width: 12 }, { width: 18 }, { width: 12 }, { width: 25 },
  ];
  s5.getRow(3).values = ['ID', 'Source Name', 'Data Type', 'Metrics Sourced', 'Refresh Freq', 'Latency', 'Owner', 'Access', 'Notes'];
  styleHeaders(s5, 3);

  // ═══ SHEET 6: Tracking Plan ═══
  const s6 = wb.addWorksheet('6. Tracking Plan', { properties: { tabColor: { argb: 'FFE85D3A' } } });
  sectionRow(s6, 1, 'EVENT TRACKING PLAN', 10);
  s6.getRow(3).values = ['', 'Implementation specification for all tracked events'];
  s6.columns = [
    { width: 8 }, { width: 25 }, { width: 35 }, { width: 30 },
    { width: 35 }, { width: 20 }, { width: 10 }, { width: 18 },
    { width: 12 }, { width: 15 },
  ];
  s6.getRow(4).values = ['ID', 'Event Name', 'Description', 'Trigger', 'Parameters', 'Page / Screen', 'Platform', 'Linked Metric', 'Status', 'Owner'];
  styleHeaders(s6, 4);

  // ═══ SHEET 7: Reporting Cadence ═══
  const s7 = wb.addWorksheet('7. Reporting Cadence', { properties: { tabColor: { argb: 'FF8B5CF6' } } });
  sectionRow(s7, 1, 'REPORTING CADENCE', 6);
  s7.getRow(3).values = ['', 'Define who gets what reports, how often'];
  s7.columns = [
    { width: 28 }, { width: 25 }, { width: 15 }, { width: 35 }, { width: 20 }, { width: 30 },
  ];
  s7.getRow(4).values = ['Report Name', 'Audience', 'Frequency', 'Key Metrics', 'Owner', 'Notes'];
  styleHeaders(s7, 4);

  // ═══ SHEET 8: RACI & Sign-off ═══
  const s8 = wb.addWorksheet('8. RACI & Sign-off', { properties: { tabColor: { argb: 'FFEF4444' } } });
  sectionRow(s8, 1, 'RACI MATRIX', 6);
  s8.columns = [
    { width: 30 }, { width: 18 }, { width: 18 }, { width: 18 }, { width: 18 }, { width: 18 },
  ];
  s8.getRow(3).values = ['Activity', 'Analytics Lead', 'Engineering', 'Product', 'Marketing', 'Executive'];
  styleHeaders(s8, 3);
  const raciRows = [
    ['Define business objectives', 'C', 'I', 'R', 'A', 'A'],
    ['Design tracking plan', 'R', 'C', 'C', 'I', 'I'],
    ['Implement events', 'C', 'R', 'I', 'I', 'I'],
    ['QA tracking', 'R', 'A', 'I', 'I', 'I'],
    ['Build dashboards', 'R', 'I', 'C', 'C', 'I'],
    ['Ongoing monitoring', 'R', 'I', 'I', 'C', 'I'],
    ['Report distribution', 'R', 'I', 'I', 'I', 'A'],
    ['Privacy / consent compliance', 'C', 'R', 'I', 'I', 'A'],
  ];
  raciRows.forEach((row, i) => {
    const r = s8.getRow(4 + i);
    row.forEach((v, j) => {
      r.getCell(j + 1).value = v;
      r.getCell(j + 1).border = allBorders;
      r.getCell(j + 1).alignment = { horizontal: 'center', vertical: 'middle' };
    });
    r.getCell(1).alignment = { horizontal: 'left', vertical: 'middle' };
  });

  sectionRow(s8, 14, 'SIGN-OFF', 5);
  s8.getRow(16).values = ['Role', 'Name', 'Decision', 'Date', 'Comments'];
  styleHeaders(s8, 16);
  ['Project Owner', 'Executive Sponsor', 'Measurement Lead', 'Head of Engineering'].forEach((role, i) => {
    const r = s8.getRow(17 + i);
    r.getCell(1).value = role;
    for (let j = 1; j <= 5; j++) r.getCell(j).border = allBorders;
  });

  // ═══ SHEET 9: Glossary ═══
  const s9 = wb.addWorksheet('9. Glossary', { properties: { tabColor: { argb: 'FF64748B' } } });
  sectionRow(s9, 1, 'GLOSSARY OF TERMS', 2);
  s9.columns = [{ width: 25 }, { width: 70 }];
  s9.getRow(3).values = ['Term', 'Definition'];
  styleHeaders(s9, 3);
  const glossary = [
    ['KPI', 'Key Performance Indicator — a measurable value that demonstrates how effectively a goal is being achieved'],
    ['GA4', 'Google Analytics 4 — Google\'s current analytics platform based on event-based data collection'],
    ['GTM', 'Google Tag Manager — a tag management system for deploying tracking code without modifying source code'],
    ['Event', 'A user interaction or system occurrence that is tracked (e.g., page_view, purchase, form_submit)'],
    ['Dimension', 'A descriptive attribute of data (e.g., country, device, campaign) used to segment metrics'],
    ['Metric', 'A quantitative measurement (e.g., sessions, conversion rate, revenue)'],
    ['Conversion', 'A completed activity that is important to the business (e.g., purchase, sign-up, form submission)'],
    ['Funnel', 'A series of steps a user takes toward a conversion goal, used to identify drop-off points'],
    ['Data Layer', 'A JavaScript object that passes information from a website to GTM/analytics tools'],
    ['Consent Mode', 'Google\'s framework for adjusting analytics behavior based on user consent status'],
    ['RACI', 'Responsible, Accountable, Consulted, Informed — a responsibility assignment matrix'],
    ['snake_case', 'A naming convention where words are separated by underscores (e.g., add_to_cart). GA4 standard.'],
    ['Custom Dimension', 'A user-defined dimension in GA4 for tracking business-specific attributes'],
    ['Session', 'A group of user interactions with a website that take place within a given time frame'],
    ['Bounce Rate', 'The percentage of sessions where the user viewed only one page and triggered no events'],
  ];
  glossary.forEach(([term, def], i) => {
    const r = s9.getRow(4 + i);
    r.getCell(1).value = term;
    r.getCell(1).font = { bold: true };
    r.getCell(2).value = def;
    r.getCell(2).alignment = { wrapText: true };
    r.getCell(1).border = allBorders;
    r.getCell(2).border = allBorders;
  });

  // Write file
  const outputPath = path.join(__dirname, '..', 'templates', 'measurement-plan-template.xlsx');
  await wb.xlsx.writeFile(outputPath);
  console.log('Template created at:', outputPath);
}

createTemplate().catch(console.error);
